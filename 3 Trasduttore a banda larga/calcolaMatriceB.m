function B = calcolaMatriceB(A, Z1)
    B_11 = A(2, 2) - (A(1, 2)^2 / (Z1 + A(1, 1)));
    B_12 = A(2, 3) - (A(1, 2) * A(1, 3)) / (Z1 + A(1, 1));
    B_21 = A(2, 3) - (A(1, 2) * A(1, 3)) / (Z1 + A(1, 1));
    B_22 = A(3, 3) - (A(1, 3)^2 / (Z1 + A(1, 1)));
    B = [
        B_11 B_12;
        B_21 B_22;
    ];
end